#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*p;

	p = (unsigned char *)b;
	i = 0;
	while (len > i)
	{
		p[i] = (unsigned char )c;
		i++;
	}
	return (b);
}
